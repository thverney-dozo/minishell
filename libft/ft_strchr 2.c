#include "libft.h"
#include <stdio.h>

char	*ft_strchr(const char *s, int c)
{
	char *str;

	if (!s)
		return (NULL);
	str = (char*)s;
	while (*str != c)
	{
		if (*str == '\0')
		{
			return (NULL);
		}
		str++;
	}
	return (str);
}
